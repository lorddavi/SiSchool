    public void checaLogin(){
        LoginDAO lDao = new LoginDAO();
        String login = txtLoginLogin.getText();
        String senha = new String(txtSenhaLogin.getPassword());
        final String respostaLogin = lDao.confereSenha(login, senha);
        switch (respostaLogin){
            case "confere":
                this.dispose();
                AbrirTelas at = new AbrirTelas();
                at.abrirJFPrincipal(lDao.usrLogado());
                break;
            case "senhaN":
                JOptionPane.showMessageDialog(null, "Senha inv√°lida!");
                break;
            case "loginN":
                JOptionPane.showMessageDialog(null, "Login Inexistente!");
                break;
        } 
    }
    
