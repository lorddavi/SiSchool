    private class OuvintesAction implements ActionListener{

        @Override
        public void actionPerformed(ActionEvent ae) {
            if (ae.getSource()==btnSeguranca){
                abrirTelas.abrirJFBackup(login);
            } else if (ae.getSource() == btnFechar){
                System.exit(0);
            } else if (ae.getSource() == btnMinimizar){
                setExtendedState(ICONIFIED);
            } else if (ae.getSource() == btnMaximizar){
                maximizar();
            } else if (ae.getSource() == btnEstatisticas){
                abrirTelas.abrirJFEstatisticas(login);
            } else if (ae.getSource() == btnCadastrarAluno){
                abrirTelas.abrirJFCadastrarAlunos(login);
            } else if (ae.getSource() == btnConsultar){
                abrirTelas.abrirJFConsultas(login);
            } else if (ae.getSource() == btnCadastrarEscola){
                abrirTelas.abrirJFCadastroEscola(login);
            } else if (ae.getSource() == btnCronogramaDeAulas){
                abrirTelas.abrirJFCronograma(login);
            } else if (ae.getSource() == btnNotasProf){
                abrirTelas.abrirJFNotasEFaltas(login);
//            } else if (ae.getSource() == btnDevCad){
//                abrirTelas.abrirJFDevCad(login);
            } else if (ae.getSource() == btnCadFunc){
                abrirTelas.abrirJFCadastrarFuncionarios(login);
            } else if (ae.getSource() == btnLogout){
                logout();
            } else if (ae.getSource() == btnAtribuirProf){
                abrirTelas.abrirJFProfTurmas(login);
            } else if (ae.getSource() == btnEstatisticas){
                abrirTelas.abrirJFTransferirAlunos(login);
            } else if (ae.getSource() == btnWarning){
                abrirTelas.abrirJFAvisos(login);
            } else if (ae.getSource() == btnProfTurmasII){
                abrirTelas.abrirJFProfTurmasPebII(login);
            } else if (ae.getSource() == btnHelp){
                 abrirPdf();
            } else if (ae.getSource() == btnTransferirAluno){
                abrirTelas.abrirJFTransferirAlunos(login);
            } else if (ae.getSource() == btnCriarTurmas){
                abrirTelas.abrirJFCriarTurmas(login);
            }
        }
        
    }
    
